public class Road {
  private int numVille1;
  private int numVille2;

  public Road(int numVille1, int numVille2) {
    this.numVille1 = numVille1;
    this.numVille2 = numVille2;
  }

  public int getNumVille1() {
    return numVille1;
  }

  public int getNumVille2() {
    return numVille2;
  }
}
